package com.quanment.app.model;

public class BusinessListBean {

    private String businessName;
    private String businessDistance;
    private String businessPrices;
    private String businessSaleCount;

    public String getBusinessName() {
        return businessName;
    }

    public void setBusinessName(String businessName) {
        this.businessName = businessName;
    }

    public String getBusinessDistance() {
        return businessDistance;
    }

    public void setBusinessDistance(String businessDistance) {
        this.businessDistance = businessDistance;
    }

    public String getBusinessPrices() {
        return businessPrices;
    }

    public void setBusinessPrices(String businessPrices) {
        this.businessPrices = businessPrices;
    }

    public String getBusinessSaleCount() {
        return businessSaleCount;
    }

    public void setBusinessSaleCount(String businessSaleCount) {
        this.businessSaleCount = businessSaleCount;
    }
}
